    public ArrayList<stulist> ListStu(String val){
        ArrayList<stulist> stuList=new ArrayList<stulist>();
        Statement st=null;
        ResultSet rs=null;
        try{
            conn=ConnectSQL.connectDB();
            st=conn.createStatement();
            String sql="select id,name,dept,booksavailed from userdet where id="+urid.getText();
            rs=st.executeQuery(sql);
            stulist sl;
            while(rs.next()){
                sl=new stulist(rs.getInt("id"),rs.getString("name"),rs.getString("dept"),rs.getInt("booksavailed"));
                stuList.add(sl);
            }
        }catch(Exception e){
            JOptionPane.showMessageDialog(null,e);
        }
        return stuList;
    }
    public void findUser(){
        ArrayList<stulist> stu=ListStu(urid.getText());
        DefaultTableModel model=(DefaultTableModel) table.getModel();
        
        Object[] row=new Object[4];
        for(stulist sl1:stu){
            row[0]=sl1.getUid();
            row[1]=sl1.getUname();
            row[2]=sl1.getDepart();
            row[3]=sl1.getBt();
            model.addRow(row);
        }
    }
