    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        conn=ConnectSQL.connectDB();
        try{
            Statement st=conn.createStatement();
            ResultSet rs=st.executeQuery("Select date from issuelist where uid='"+user.getText()+"' and bid='"+bkid.getText()+"'");
            while(rs.next()){
                String d=rs.getString(1);
                idate.setText(d);
            }
        }catch(Exception e){
            JOptionPane.showMessageDialog(null,e);
        }
    }//GEN-LAST:event_jButton1ActionPerformed

    private void retActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_retActionPerformed
        // TODO add your handling code here:
        conn=ConnectSQL.connectDB();
        try{
            Statement st=conn.createStatement();
            JOptionPane.showMessageDialog(null,"Book returned");
            st.executeUpdate("update bookdet set quan=quan+1 where bid='"+bkid.getText()+"'");
            st.executeUpdate("update userdet set booksavailed=booksavailed-1 where id='"+user.getText()+"'");
            st.executeUpdate("delete from issuelist where uid='"+user.getText()+"' and bid='"+bkid.getText()+"'");
        }catch(Exception e){
            JOptionPane.showMessageDialog(null,e);
        }
    }//GEN-LAST:event_retActionPerformed

