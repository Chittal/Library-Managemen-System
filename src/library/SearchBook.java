    public ArrayList<booklist> ListBook(String val){
        ArrayList<booklist> bookList=new ArrayList<booklist>();
        Statement st=null;
        ResultSet rs=null;
        try{
            conn=ConnectSQL.connectDB();
            st=conn.createStatement();
            String sql="select bid,btitle,auth,cate,quan from bookdet where cate='"+category.getText()+"'";
            rs=st.executeQuery(sql);
            booklist bl;
            while(rs.next()){
                bl=new booklist(rs.getInt("bid"),rs.getString("btitle"),rs.getString("auth"),rs.getString("cate"),rs.getInt("quan"));
                bookList.add(bl);
            }
        }catch(Exception e){
            JOptionPane.showMessageDialog(null,e);
        }
        return bookList;
    }
    public void findBook(){
        ArrayList<booklist> bk=ListBook(category.getText());
        DefaultTableModel model=(DefaultTableModel) table1.getModel();
        
        Object[] row=new Object[5];
        for(booklist bl1:bk){
            row[0]=bl1.getBoid();
            row[1]=bl1.getBoname();
            row[2]=bl1.getBoauth();
            row[3]=bl1.getBocat();
            row[4]=bl1.getAv();
            model.addRow(row);
        }
    }
